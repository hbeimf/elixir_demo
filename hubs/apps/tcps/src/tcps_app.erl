-module(tcps_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
	% {_Ip, Port} = rconf:read_config(hub_server),
	Port = 9910,
	{ok, _} = ranch:start_listener(tcp_server, 10, ranch_tcp, [{port, Port}], tcp_handler, []),
    tcps_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

